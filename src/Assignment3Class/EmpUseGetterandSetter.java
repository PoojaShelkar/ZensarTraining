package Assignment3Class;

public class EmpUseGetterandSetter {

	 private String name; 
	  private int id; 
	  private String nCompany; 
	  EmpUseGetterandSetter(String name, int id, String nCompany)
	 { 
	   this.id = id; 
	   this.name = name; 
	   this.nCompany = nCompany; 
	 } 
	 public String getName() { 
	   return name; 
	 } 
	public int getId() { 
	   return id; 
	 } 
	public String getNCompany() { 
	   return nCompany;
	 } 
	
	 } 
	