package BubbleSort;

public class AsscendingElement {

	public static void main(String[] args) {
		
		
		int[] a = {124,45,6,78,0,-1};
		for(int k=0;k<a.length;k++)
		{
			for(int j=0;j<a.length;j++)
			{
				if(a[j]>a[j+1])
				{
					int t=a[j];
					a[j]=a[j+1];
					a[j+1]=t;
					
				}
				
			}
		
			System.out.println(Array,toString(a));
			
		}
		System.out.println(Array,toString(a));
		
}
}
	

	
	

	
	


